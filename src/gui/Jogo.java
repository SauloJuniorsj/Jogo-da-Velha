    public void verificaJogadordaVez(){
        if(joga && jogb){
            this.op = 1;
            joga = false;
            jogb = true;
            txtvisor.setText("Vez do jogador 2");
        } else if (joga && jogb == false){
            this.op = 1;
            joga = false;
            jogb = true;
            txtvisor.setText("Vez do jogador 2");
        } else {
            this.op = 2;
            joga = true;
            jogb = false;
            txtvisor.setText("Jogador(a) da vez " + this.nome);
        }
    }
    
